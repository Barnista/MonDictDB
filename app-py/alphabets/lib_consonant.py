    def get_all(self):
        return self.consonants

    def get_by_position(self, row, column):
        r = len(self.consonants) - 1 if row is None else row
        c = 0 if column is None else column
        return self.consonants[r]["rows"][c]

    def get_by_letter(self, letter):
        for consonant in self.consonants:
            for row in consonant["rows"]:
                if row["letter"] == letter or row.get("letter2") == letter:
                    return row
        return None

    def is_consonant(self, letter):
        consonant = self.get_by_letter(letter)
        return bool(consonant)

    def plots(self):
        arr = []
        for i_r, consonant in enumerate(self.consonants):
            for i_c, row in enumerate(consonant["rows"]):
                arr.append({
                    "row": i_r,
                    "column": i_c,
                    "letter": row["letter"],
                    "sound": row["sound"],
                    "compoundIPA": row["compoundIPA"],
                    "compoundTH": row["compoundTH"]
                })
        return arr

